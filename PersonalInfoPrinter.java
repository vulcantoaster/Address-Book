public class PersonalInfoPrinter {
    public static void main(String[] args) {
        
        // 1. Define and Assign Fictional Values to String Variables
        String firstName = "Alex";
        String lastName = "Rivera";
        String streetAddress = "123 Main St";
        String city = "Anytown";
        String zipCode = "90210";
        
        // 2. Output Each Piece of Information on a New Line
        // The println() method automatically adds a newline character after the output.
        System.out.println(firstName);
        System.out.println(lastName);
        System.out.println(streetAddress);
        System.out.println(city);
        System.out.println(zipCode);
    }
}